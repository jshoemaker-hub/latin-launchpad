function buildGradeWords(firstNouns, secondNouns, firstVerbs, secondVerbs) {
  const seen = new Set();
  return [firstNouns, secondNouns, firstVerbs, secondVerbs]
    .flat()
    .reduce((words, [latin, english, emoji = '']) => {
      const key = `${latin.toLowerCase()}|${english.toLowerCase()}`;
      if (seen.has(key)) return words;
      seen.add(key);
      words.push({ latin, english, emoji });
      return words;
    }, []);
}

